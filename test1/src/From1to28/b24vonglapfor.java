package From1to28;

//cau truc lap for
// biet trc so vong lap
/*
        for(khoi tao,dieu kien lap,buoc nhay)
                do sth
 */
public class b24vonglapfor {
    public static void main(String[] args) {
        for (int i = 100; i >= 0; i -= 4) {
            if (i % 5 == 0) {
                System.out.println(i);
            }

        }
    }
}
